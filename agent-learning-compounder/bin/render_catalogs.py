#!/usr/bin/env python3
"""Render catalog markdown from Python registries (KTD-20)."""

from __future__ import annotations

import argparse
import importlib
import json
import pathlib
import sys
from dataclasses import asdict, is_dataclass
from typing import Any

ROOT = pathlib.Path(__file__).resolve().parents[1]
DEFAULT_OUTPUT_DIR = ROOT / "skills"
for _path in (ROOT, ROOT / "bin"):
    if str(_path) not in sys.path:
        sys.path.insert(0, str(_path))


def _to_entries(obj: Any) -> list[dict[str, Any]]:
    if obj is None:
        return []
    if isinstance(obj, dict):
        values = list(obj.values())
        return [entry for entry in (_entry_to_dict(value) for value in values) if entry]
    if isinstance(obj, (list, tuple)):
        return [entry for entry in (_entry_to_dict(item) for item in obj) if entry]
    return []


def _entry_to_dict(entry: Any) -> dict[str, Any]:
    if isinstance(entry, dict):
        return entry
    if hasattr(entry, "catalog_entry"):
        return dict(entry.catalog_entry())
    if is_dataclass(entry):
        return asdict(entry)
    return {}


def _safe_get(entry: dict[str, Any], name: str, default: str = "") -> Any:
    value = entry.get(name, default)
    if callable(value):
        module = getattr(value, "__module__", "")
        name = getattr(value, "__name__", repr(value))
        return f"{module}.{name}" if module else name
    return value


def _readme_header(name: str, source: str) -> str:
    return (
        f"# {name}\n"
        "\n"
        "Generated from Python registry:\n"
        f"`{source}`\n"
        "\n"
    )


def _render_table(entries: list[dict[str, Any]], headers: list[str]) -> str:
    if not entries:
        return ""
    header = "| " + " | ".join(headers) + " |\n"
    sep = "| " + " | ".join("---" for _ in headers) + " |\n"
    rows: list[str] = []
    for entry in entries:
        values = []
        for header_name in headers:
            value = _safe_get(entry, header_name, "")
            if isinstance(value, (dict, list)):
                value = json.dumps(value, sort_keys=True)
            values.append(str(value).replace("|", "\\|"))
        rows.append("| " + " | ".join(values) + " |")
    return header + sep + "\n".join(rows) + "\n"


def _inline_code_list(values: Any) -> str:
    if not isinstance(values, list):
        values = list(values) if isinstance(values, tuple) else [values]
    return ", ".join(f"`{str(value)}`" for value in values)


def _render_analyst_queries_catalog(entries: list[dict[str, Any]]) -> str:
    headers = ["Catalog ID", "Question", "Consumer", "SQL skeleton", "Output shape"]
    rows: list[str] = []
    for entry in entries:
        sql = str(entry["sql"]).replace("|", "\\|")
        rows.append(
            "| "
            + " | ".join(
                [
                    str(entry["id"]),
                    str(entry["question"]),
                    _inline_code_list(entry["consumers"]),
                    f"`{sql}`",
                    _inline_code_list(entry["shape"]),
                ]
            )
            + " |"
        )
    return (
        "# Analyst queries catalog\n"
        "\n"
        "| " + " | ".join(headers) + " |\n"
        + "| " + " | ".join("---" for _ in headers) + " |\n"
        + "\n".join(rows)
        + "\n"
    )


def _render_mcp_catalog(entries: list[dict[str, Any]]) -> str:
    rows: list[str] = []
    for entry in entries:
        rows.append(
            "| "
            + " | ".join(
                [
                    str(entry["id"]),
                    f"`{entry['name']}`",
                    str(entry["kind"]),
                    f"`{entry['backing']}`",
                    str(entry["summary"]).replace("|", "\\|"),
                ]
            )
            + " |"
        )
    return (
        f"# MCP Capability Catalog (M1-M{len(entries)})\n"
        "\n"
        "`alc_mcp.catalog.MCP_TOOLS` is the canonical machine-readable catalog. "
        "This file is generated by `bin/render_catalogs.py`; regenerate it instead "
        "of hand-editing tool rows.\n"
        "\n"
        "| ID | Tool | Kind | Backing | Notes |\n"
        "| --- | --- | --- | --- | --- |\n"
        + "\n".join(rows)
        + "\n"
        "\n"
        "In addition to the catalog tools, the server exposes a meta tool "
        "`list_capabilities(repo)` that returns the live catalog plus per-tool "
        "`version` and `min_compatible_version`. Agents should call it first and "
        "compare against their known client contract before invoking other tools.\n"
    )


def _render_catalog_payload(name: str, module_path: str, attr: str) -> tuple[str, int]:
    module = importlib.import_module(module_path)
    source = getattr(module, attr)
    entries = _to_entries(source)
    if not entries:
        raise RuntimeError(f"{module_path}.{attr} was empty or unsupported")

    if module_path == "alc_mcp.catalog" and attr == "MCP_TOOLS":
        if isinstance(source, dict):
            for entry, tool_name in zip(entries, source):
                entry.setdefault("name", tool_name)
        return _render_mcp_catalog(entries), len(entries)

    if module_path == "bin.analyst_queries" and attr == "QUERIES":
        return _render_analyst_queries_catalog(entries), len(entries)

    headers = ["id", "kind", "summary", "backing", "version"]
    if module_path == "bin.recommender_generators" and attr == "GENERATORS":
        headers = ["id", "kind", "summary", "backing", "version", "output_class", "target_type", "policy_owner"]
    body = _render_table(entries, headers)
    return _readme_header(name, f"{module_path}.{attr}") + body, len(entries)


def _render_catalog(name: str, module_path: str, attr: str, output_path: pathlib.Path) -> tuple[str, int]:
    payload, count = _render_catalog_payload(name, module_path, attr)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(payload, encoding="utf-8")
    return (str(output_path), count)


def _default_specs() -> list[tuple[str, str, str, pathlib.Path]]:
    return [
        ("mcp-catalog", "alc_mcp.catalog", "MCP_TOOLS", ROOT / "reference-lib" / "mcp-catalog"),
        ("mcp-catalog", "alc_mcp.catalog", "MCP_TOOLS", ROOT / "skills" / "alc-core" / "references" / "mcp-catalog.md"),
        ("query-catalog", "bin.analyst_queries", "QUERIES", ROOT / "reference-lib" / "analyst-queries-catalog"),
        ("generator-catalog", "bin.recommender_generators", "GENERATORS", ROOT / "reference-lib" / "generator-catalog"),
        ("generator-catalog", "bin.recommender_generators", "GENERATORS", ROOT / "skills" / "alc-core" / "references" / "generator-catalog.md"),
        ("propose-catalog", "bin.analyst_queries", "PROPOSALS", ROOT / "skills" / "alc-core" / "references" / "propose-catalog.md"),
    ]


def render_catalogs(specs: list[tuple[str, str, str, pathlib.Path]] | None = None) -> list[tuple[str, int]]:
    catalog_specs = specs or _default_specs()
    rendered: list[tuple[str, int]] = []
    for name, module_name, attr, output in catalog_specs:
        try:
            path, count = _render_catalog(name, module_name, attr, pathlib.Path(output))
            rendered.append((path, count))
        except ModuleNotFoundError:
            # Optional registries may not exist yet in early phases.
            continue
        except RuntimeError as exc:
            if "was empty or unsupported" not in str(exc):
                raise
            # Optional registries may exist before they have entries.
            continue
    return rendered


def render_all(specs: list[tuple[str, str, str, pathlib.Path]] | None = None) -> list[tuple[str, int]]:
    return render_catalogs(specs)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="Reserved for drift check mode")
    args = parser.parse_args(argv)

    rendered = render_all()
    for path, count in rendered:
        print(f"rendered {path}: {count}")
    if not rendered:
        print("rendered 0 catalogs: no registries discovered")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
